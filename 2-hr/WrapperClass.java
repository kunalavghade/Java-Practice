/*
Wrapper Class
	1.	Provides a way to use primitive data types as referece data types 
	2.	Referece data types contains useful methods
	3.	Can be used with collections (ex.ArrayList) 

Primitive	|	Wrapper
boolean 	|	Boolean
char 		| 	Character
int 		| 	Integer
double 		| 	Double

Autoboxing = the automatic conversation that Java compiler makes between the primitive and there correspondig Wrapper class.
unboxing = the reverse of autoboxing.


*/

public class WrapperClass{
	public static void main(String[] args) {
		
		Boolean a = true;
		Character c = "@";
		Integer i = 123;
		Double d = 3.14;
		String s = "Kunal"; 
	}
}